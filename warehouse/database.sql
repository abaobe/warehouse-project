    SELECT COUNT(*) INTO records_number FROM users WHERE
      username like search_val OR first_name||' '||middle_name||' '||last_name like search_val;
    
    RETURN records_number;
  END get_users_count;
--------------------------------------------------------------
END USER_ACTIONS;

